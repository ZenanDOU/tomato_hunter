import type { SpriteData, SpriteFrame } from "../types";

// Compact encoding: each char is a palette index (0-9, a=10, b=11, c=12)
// Parse 32-char lines into number[] rows
function parse(lines: string, palette: string[]): SpriteData {
  const rows = lines.trim().split('\n').map(line => {
    const row: number[] = [];
    for (const ch of line) {
      if (ch >= '0' && ch <= '9') row.push(parseInt(ch));
      else if (ch >= 'a' && ch <= 'f') row.push(ch.charCodeAt(0) - 87);
      else row.push(0);
    }
    // Pad to 32 if needed
    while (row.length < 32) row.push(0);
    return row;
  });
  while (rows.length < 32) rows.push(new Array(32).fill(0));

  const idle0 = rows;
  const idle1 = deriveBreathing(idle0);
  const hit0 = deriveHit(idle0, palette);
  const defeat0 = deriveDefeat1(idle0);
  const defeat1 = deriveDefeat2(idle0);

  return {
    palette,
    frames: {
      idle: [idle0, idle1],
      hit: [hit0],
      defeat: [defeat0, defeat1],
    },
  };
}

// Idle frame 2: shift pixels down by 1px (breathing effect)
function deriveBreathing(frame: SpriteFrame): SpriteFrame {
  const result: SpriteFrame = [new Array(32).fill(0)];
  for (let y = 0; y < 31; y++) {
    result.push([...frame[y]]);
  }
  return result;
}

// Hit frame: replace all non-zero non-outline pixels with white (last palette index)
function deriveHit(frame: SpriteFrame, palette: string[]): SpriteFrame {
  const whiteIdx = palette.length - 1;
  return frame.map(row =>
    row.map(px => (px > 1 ? whiteIdx : px)) // keep 0 (transparent) and 1 (outline)
  );
}

// Defeat frame 1: shift down 2px
function deriveDefeat1(frame: SpriteFrame): SpriteFrame {
  const result: SpriteFrame = [];
  result.push(new Array(32).fill(0));
  result.push(new Array(32).fill(0));
  for (let y = 0; y < 30; y++) {
    result.push([...frame[y]]);
  }
  return result;
}

// Defeat frame 2: shift down 4px + dissolve bottom half
function deriveDefeat2(frame: SpriteFrame): SpriteFrame {
  const result: SpriteFrame = [];
  for (let i = 0; i < 4; i++) result.push(new Array(32).fill(0));
  for (let y = 0; y < 28; y++) {
    const row = [...frame[y]];
    // Dissolve: rows below 20 become increasingly transparent
    if (y + 4 > 20) {
      const dissolveChance = (y + 4 - 20) / 12;
      for (let x = 0; x < 32; x++) {
        if (Math.random() < dissolveChance) row[x] = 0;
      }
    }
    result.push(row);
  }
  return result;
}

